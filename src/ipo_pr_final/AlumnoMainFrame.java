    public void loadAsignaturas()
    {
        JPanel row = new JPanel();
        row.setBackground(new Color(0xfafafa));
        row.setLayout(new GridBagLayout());
        
        GridBagConstraints c = new GridBagConstraints();
        c.insets = new Insets(4,10,10,4);
        
        String[] fields = BD_Helper.getStudentLine(userlog).split(";");
        /* NOMBRE;CONTRASE√ëA;ROL;EMAIL;ASIGNATURA;GRUPO;SUBGRUPO;ASIGNATURA...*/
        JLabel _asig_name = new JLabel(" ASIGNATURA ", SwingConstants.CENTER);
        _asig_name.setFont(new Font("tahoma", Font.PLAIN, 16));
        c.fill = GridBagConstraints.HORIZONTAL;
        c.gridx = 0;
        c.gridy = 0;
        _asig_name.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.black));
        row.add(_asig_name,c);

        JLabel _grupo = new JLabel(" GR. PR√ÅCTICAS ", SwingConstants.CENTER);
        _grupo.setFont(new Font("tahoma", Font.PLAIN, 16));
        c.fill = GridBagConstraints.HORIZONTAL;
        c.gridx = 1;
        c.gridy = 0;
        _grupo.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.black));
        row.add(_grupo,c);

        JLabel _grupopr = new JLabel(" GR. TRABAJO ", SwingConstants.CENTER);
        _grupopr.setFont(new Font("tahoma", Font.PLAIN, 16));
        c.fill = GridBagConstraints.HORIZONTAL;
        c.gridx = 2;
        c.gridy = 0;
        _grupopr.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.black));
        row.add(_grupopr,c);
        
        JLabel _grupomembers = new JLabel(" COMPA√ëEROS DE GRUPO ", SwingConstants.CENTER);
        _grupomembers.setFont(new Font("tahoma", Font.PLAIN, 16));
        c.fill = GridBagConstraints.HORIZONTAL;
        c.gridx = 3;
        c.gridy = 0;
        _grupomembers.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.black));
        row.add(_grupomembers,c);
        
        int contador=1;
        for(int i=4; i<fields.length; i+=5){
            String sasig_name = (fields[i].compareTo("-1")==0)? "Sin asignar":fields[i];
            JLabel asig_name = new JLabel(sasig_name);
            asig_name.setFont(new Font("tahoma", Font.PLAIN, 14));
            c.fill = GridBagConstraints.HORIZONTAL;
            c.gridx = 0;
            c.gridy = contador;
            row.add(asig_name,c);
            
            String sgrupo = (fields[i+1].compareTo("-1")==0)? "Sin asignar":fields[i+1];
            JLabel grupo = new JLabel(sgrupo, SwingConstants.CENTER);
            if(fields[i+3].compareTo("-1")!=0 && fields[i+1].compareTo(fields[i+3])!=0){
                sgrupo += " (->"+fields[i+3].split(":")[0]+")";
                grupo.setText(sgrupo);
                grupo.setBackground(Color.yellow);
            }
            grupo.setFont(new Font("tahoma", Font.PLAIN, 14));
            c.fill = GridBagConstraints.HORIZONTAL;
            c.gridx = 1;
            c.gridy = contador;
            row.add(grupo,c);
            
            String sgrupopr = (fields[i+2].compareTo("-1")==0)? "Sin asignar":fields[i+2];
            JLabel grupopr = new JLabel(sgrupopr, SwingConstants.CENTER);
            if(fields[i+4].compareTo("-1")!=0 && fields[i+2].compareTo(fields[i+4])!=0){
                sgrupopr += " (->"+fields[i+4]+")";
                grupopr.setText(sgrupopr);
                grupopr.setBackground(Color.yellow);
            }
            grupopr.setFont(new Font("tahoma", Font.PLAIN, 14));
            c.fill = GridBagConstraints.HORIZONTAL;
            c.gridx = 2;
            c.gridy = contador;
            row.add(grupopr,c);
            
            String sgrupocompas[] = BD_Helper.getCompas(fields[i], fields[i+1], fields[i+2]);
            if(sgrupocompas != null && sgrupocompas.length>0){
                boolean firstE = true;
                String sgcompas = "";
                for(int iii=0; iii<sgrupocompas.length; iii++){
                    if(sgrupocompas[iii].compareTo(BD_Helper.getName(userlog))!=0){
                        if(firstE){
                            sgcompas+=sgrupocompas[iii];
                            firstE = false;
                        }else{
                            sgcompas+=", "+sgrupocompas[iii];
                        }
                    }
                }
                JLabel grupocompas = new JLabel(sgcompas, SwingConstants.CENTER);     
                grupocompas.setFont(new Font("tahoma", Font.PLAIN, 14));
                c.fill = GridBagConstraints.HORIZONTAL;
                c.gridx = 3;
                c.gridy = contador;
                row.add(grupocompas,c);
            }
            
            contador++;
        }
        
        jPanel3.add(new JScrollPane(row), BorderLayout.PAGE_START);
    }
    
